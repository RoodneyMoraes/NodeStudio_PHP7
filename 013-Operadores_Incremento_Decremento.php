<?php
//Operador de Incremento

$valor1 = 10;

//Pré-Incremento
echo "O valor1 é $valor1";
echo "<br>";
echo "O valor1 com o pré-incremento é ". ++$valor1;
echo "<hr>";




$valor2 = 20;

//Pós-Incremento
echo "O valor2 é $valor2";
echo "<br>";
echo "O valor2 é ". $valor2++. " com o pós-incremento é $valor2";
echo "<hr>";




$valor3 = 30;

//Pré-Decremento
echo "O valor3 é $valor3";
echo "<br>";
echo "O valor3 com o pré-decremento é ". --$valor3;
echo "<hr>";





$valor4 = 40;

//Pós-Decremento
echo "O valor4 é $valor4";
echo "<br>";
echo "O valor4 é ". $valor4--. " com o pós-decremento é $valor4";
echo "<hr>";

?>